char *ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	while (src[i])
	{
		dst[i] = (char)src[i];
		++i;
	}
	if (src[i] == 0)
		dst[i] = 0;
	return (dst);
}
