int		ft_i_strrchr(char *s, int c)
{
	int occ;
	int i;

	i = 0;
	occ = -1;
	while (s[i])
	{
		if (s[i] == (char)c)
			occ = i;
		++i;
		if ((s[i] == (char)c) && (c == 0))
			return (i);
	}
	return (occ);
}
